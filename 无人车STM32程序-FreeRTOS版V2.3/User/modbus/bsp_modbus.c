/* 参  数：
 * puchMsg  DD const BYTE*，指向待校验数据存储区的首地址
 * usDataLen DD 待校验数据的字节个数
 * 返回值：WORD型，计算出的CRC值。   */
uint16_t CalCrcFast(const uint8_t* puchMsg , uint16_t usDataLen)
{
        uint8_t uchCRCHi = 0xFF ;  /* CRC高字节初始化 */
        uint8_t uchCRCLo = 0xFF ;  /* CRC低字节初始化 */
        uint16_t uIndex;           /* CRC查表的索引*/
                   
        while (usDataLen--)             
        {
                  uIndex = uchCRCHi ^ *puchMsg++ ;       /* 计算 CRC */
                  uchCRCHi = uchCRCLo ^ auchCRCHi[uIndex];
                  uchCRCLo = auchCRCLo[uIndex] ;
        }
        return (uchCRCHi << 8 | uchCRCLo) ; 
}


 /**
	* @brief  需要通过modbus发送给机械臂的数据
	* @param  id-站号 fun-功能码  address-地址  data-数据
  * @retval 返回数据指针
	* 暂时只支持0x05写单线圈和0x06写保持寄存器
  */
static uint8_t* Send_Data_modbus(uint8_t id,uint8_t fun,uint16_t address,uint16_t data)
{
	static uint8_t send_buff[8];//原来是8  总共8个字节
	uint16_t crc = 0;
	send_buff[0] = id;
	send_buff[1] = fun;
	send_buff[2] = (address>>8)&0xff; //取高位
	send_buff[3] = address & 0xff;  //取低位
	send_buff[4] = (data>>8)&0xff;  //取高位
	send_buff[5] = data & 0xff;  //取低位
	
	
	if(fun == 0x05)//写单线圈
	{
		
		if(data > 0)
		{
			send_buff[4] = 0xFF;  
			send_buff[5] = 0x00;  
		}
		else if(data <= 0)
		{
			send_buff[4] = 0x00;  
			send_buff[5] = 0x00; 
		}
	}
	
	
	crc = CalCrcFast(send_buff,6);//CRC16校验
	send_buff[6] = (crc>>8)&0xff; //取高位
	send_buff[7] = crc&0xff; //取低位
	
	return send_buff;
}
/**
* @brief  发送数据给机械臂
	* @param  id-站号  fun-功能码 address-地址  data-数据
  * @retval 无
	* 暂时只支持0x05写单线圈和0x06写保持寄存器
  */
 void Send_Msg_toRobot(uint8_t id,uint8_t fun,uint16_t address,uint16_t data)
{
	uint8_t* str = NULL;
	str = Send_Data_modbus( id, fun, address, data);
	Usart_SendStr_length(CAMERA_USART,str,8);
	Usart_SendStr_length(SCREEN_USART,str,8);
	
}


