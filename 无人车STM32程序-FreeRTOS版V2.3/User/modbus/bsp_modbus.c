 /** 数据转换程序
 * 函数：Float2Hex
 * 由于机械臂当写在序号GV开头的全局变量时，使用的是32位的大端。所以需要转换
 * 参数：要写入全局变量中的十进制值
 * 返回值：要写入全局变量中的16进制值
 */
uint32_t data2hex(int16_t data)  
{
	uint32_t temp;  
	uint8_t i=0;
	char flag = 0;//正负标志位，正0，负1

	if(data < 0)
	{
		flag = 1;
		data = ~data+1;//变回正数先
	}
	else if(data==0)
	{
		return (uint32_t)0;
	}
	temp = (uint32_t)data;//转存需要计算的数值  
	while(temp)  
	{         
		temp >>= 1;  
		i++;//计算当前值的尾数占有的位数  
	}  
	i--;//计算下来，i会多加一次，这儿减掉  相当于阶码，阶次
	data = data & (~((uint32_t)1 << i));//清除最高位的1
	temp = data;  //再次赋值，准备合并
	temp = temp<<(23-i);//补足23位尾数  
	temp = (i+127)<<23 | temp;//计算指数，并与尾数合并起来
	
	if(flag == 1)
	{
		temp = temp | (((uint32_t)1<<31));//确定负数
	}
	else
	{
		temp = temp & (~((uint32_t)1<<31));//确定正数
	}  

	return temp;//这里就已经是以浮点数的存储方式表示的传进来的参数了
}

